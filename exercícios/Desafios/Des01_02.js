const prompt = require ('prompt-sync') ()
let saudacao = Number(prompt("Digite um numero"))

if(saudacao = 5 && saudacao <= 11){console.log("Bom dia!")}
else if(saudacao = 12 && saudacao <= 17){console.log("Boa tarde!")}
else if(saudacao = 18 && saudacao <= 23){console.log("Boa noite!")}
else if(saudacao = 0 && saudacao <= 4){console.log("Boa madrugada!")}